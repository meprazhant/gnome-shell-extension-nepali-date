function ADTOBS(year, month, day) {
    var MIN_AD_YEAR = 1943;
    var MIN_AD_MONTH = 3;
    var MIN_AD_DAY = 14;
    var MIN_BS_YEAR = 2000;
    var MILLS_IN_DAY = 86400000;
    var MAX_DAY_DIFF = 36898;

    var START_DATE = new Date(MIN_AD_YEAR, MIN_AD_MONTH, MIN_AD_DAY);
    if (!year) year = new Date().getUTCFullYear();
    if (!month && month !== 0) month = new Date().getUTCMonth();
    if (!day) day = new Date().getUTCDate();

    var dayDiff = Math.ceil((new Date(year, month, day).getTime() - START_DATE.getTime()) / MILLS_IN_DAY);
    if (dayDiff < 0 || dayDiff > MAX_DAY_DIFF) return "Invalid Date out of range";

    var year_bs = String(MIN_BS_YEAR);
    var totalCount = 0;
    var countUptoPreviousYear = 0;
    for (var [key, value] of Object.entries(numberOfDaysEachYearBS)) {
        countUptoPreviousYear = totalCount;
        totalCount += value;
        if (totalCount > dayDiff) {
            year_bs = key;
            break;
        }
    }

    var nthDayofYear = dayDiff - countUptoPreviousYear + 1;
    var total_count_month_wise = 0;
    var countUptoPreviousMonth = 0;
    var month_bs = 0;
    for (var i = 0; i < numberOfDaysEachMonthBS[year_bs].length && nthDayofYear > total_count_month_wise; i++) {
        countUptoPreviousMonth = total_count_month_wise;
        total_count_month_wise += numberOfDaysEachMonthBS[year_bs][i];
        if (countUptoPreviousMonth > 0) month_bs++;
    }

    var day_bs = nthDayofYear - countUptoPreviousMonth;
    var monthStr = (month_bs + 1) < 10 ? "0" + (month_bs + 1) : "" + (month_bs + 1);
    var dayStr = day_bs < 10 ? "0" + day_bs : "" + day_bs;
    return `${year_bs}-${monthStr}-${dayStr}`;
}

