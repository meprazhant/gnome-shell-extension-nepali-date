var nepaliNumerals = ['०', '१', '२', '३', '४', '५', '६', '७', '८', '९'];

function engtonpNumber(n) {
    var str = n.toString();
    var result = '';
    for (var i = 0; i < str.length; i++) {
        var char = str[i];
        if (char >= '0' && char <= '9') result += nepaliNumerals[parseInt(char)];
        else result += char;
    }
    return result;
}

function momentnp(date) {
    var d = new Date(date);
    if (isNaN(d.getTime())) d = new Date();

    return {
        format: function (format) {
            var year = d.getFullYear();
            var month = d.getMonth() + 1;
            var day = d.getDate();
            if (format === 'DDMMYYnp') return `${engtonpNumber(day)} ${englishMonthsnp[d.getMonth()]}, ${engtonpNumber(year)}`;
            return `${year}-${month}-${day}`;
        },
        inBS: function (format) {
            var bsStr = ADTOBS(d.getFullYear(), d.getMonth(), d.getDate());
            if (bsStr.includes("Invalid")) return bsStr;

            var parts = bsStr.split('-');
            var year_bs = parts[0];
            var month_bs = parseInt(parts[1]);
            var day_bs = parseInt(parts[2]);
            var monthName = MONTH_NAME_BS[month_bs - 1];

            if (format === 'YYMMDD') return bsStr;
            if (format === 'ddmmyy') return `${day_bs} ${monthName}, ${year_bs}`;
            if (format === 'ddmmyyfull') return `${engtonpNumber(day_bs)} ${monthName}, ${engtonpNumber(year_bs)}`;
            if (format === 'yymmdd') return `${year_bs}/${parts[1]}/${parts[2]}`;
            if (format === 'yymmddnp') return `${engtonpNumber(year_bs)}/${engtonpNumber(parts[1])}/${engtonpNumber(parts[2])}`;

            return `${day_bs} ${monthName}, ${year_bs}`;
        }
    };
}

